drop table if exists TICKET, MATCHES, TEAM, FAN, STADIUM;

CREATE TABLE STADIUM (
STADIUMID CHAR(3) CONSTRAINT STADIUM_PK PRIMARY KEY,
STADIUMNAME VARCHAR(30),
STADIUMCITY VARCHAR(20),
CAPACITY INT check (CAPACITY>0),
OPENYEAR INT check (OPENYEAR>0 and OPENYEAR  < (extract(year from CURRENT_DATE) + 1))
);

CREATE TABLE FAN (
FANID CHAR(9) CONSTRAINT FAN_PK primary key,
FANNAME VARCHAR(25),
FANNATIONALITY VARCHAR(30),
FANCATEGORY CHAR(7)
);

CREATE TABLE TEAM (
TEAMID CHAR(4) constraint TEAM_PK primary key,
COUNTRY VARCHAR(20),
CONTINENT VARCHAR(15),
FIFARANK SMALLINT,
TEAMGROUP CHAR(1),
MANAGER VARCHAR(20),
NICKNAME VARCHAR(25)
);

CREATE TABLE MATCHES (
MATCHID CHAR(5),
FIRSTTEAM CHAR(4),
SECONDTEAM CHAR(4),
MATCHDATE DATE,
KICKOFF TIME,
STADIUMID CHAR(3),
constraint MATCHES_PK primary key (MATCHID),
constraint MATCHES_FK1 foreign key (FIRSTTEAM) references TEAM(TEAMID),
constraint MATCHES_FK2 foreign key (SECONDTEAM) references TEAM(TEAMID),
constraint MATCHES_FK3 foreign key (STADIUMID) references STADIUM(STADIUMID)
);

CREATE TABLE TICKET (
TICKETID CHAR(15),
BLOCKNO CHAR(4),
ROWNO CHAR(4),
SEATNO CHAR(4),
PRICE SMALLINT,
MATCHID CHAR(5),
FANID CHAR(9),
CONSTRAINT TICKET_PK PRIMARY KEY (TICKETID),
CONSTRAINT TICKET_FK1 FOREIGN KEY (MATCHID) REFERENCES MATCHES (MATCHID),
CONSTRAINT TICKET_FK2 FOREIGN KEY (FANID) REFERENCES FAN (FANID)
);

