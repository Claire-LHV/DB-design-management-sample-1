drop function if exists FANMATCHES(in NAME_OF_FAN VARCHAR(25));

create FUNCTION FANMATCHES(in NAME_OF_FAN VARCHAR(25))
returns TABLE(MATCHID CHAR(5), STADNAME VARCHAR(30), SEAT CHAR(4), KICKOFFTIME TIME)
language 'plpgsql'
as $BODY$
begin 
return QUERY SELECT MATCHES.MATCHID, STADIUMNAME, SEATNO, KICKOFF
FROM FAN natural join ticket natural join MATCHES natural JOIN STADIUM
WHERE FANNAME=NAME_OF_FAN;
end;
$BODY$;

